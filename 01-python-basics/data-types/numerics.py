#Numbers in Python
#Numbers are a fundamental data type in Python, used to represent numeric values.
#There are three main types of numbers in Python:
#1. Integers (int): Whole numbers without a decimal point. They can be positive, negative, or zero.
#2. Floating-point numbers (float): Numbers that contain a decimal point. They can also
#   be positive, negative, or zero.
#3. Complex numbers (complex): Numbers that have a real part and an imaginary part, 
#   represented as a + bj, where a is the real part and b is the imaginary part.
#Here are some examples of each type of number in Python:
# Integer
integer_number = 42
print(f"Integer: {integer_number}")
# Floating-point number
floating_number = 3.14
print(f"Floating-point number: {floating_number}")
# Complex number
complex_number = 2 + 3j
print(f"Complex number: {complex_number}")
#You can perform various operations with numbers in Python, such as addition, subtraction, multiplication, and division.
# Here are some examples of basic arithmetic operations:
a = 10
b = 5
print(f"Addition: {a} + {b} = {a + b}")
print(f"Subtraction: {a} - {b} = {a - b}")  
print(f"Multiplication: {a} * {b} = {a * b}")
print(f"Division: {a} / {b} = {a / b}")
#You can also use the modulus operator (%) to find the remainder of a division operation.   
print(f"Modulus: {a} % {b} = {a % b}")
